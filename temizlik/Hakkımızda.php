          <h2>Hakkımızda.</h2>
          <hr>
          <div class="temizle"></div>
          <p>Alp Temizlik olarak yaptığımız tüm temizlik işleri her zaman kaliteli ve hijyen odaklı olarak yapılmıştır.
		  Çalışmalarımız uzun yıllardır aynı personelle birlikte günümüze kadar gelmiş ve gerekli alt yapıya sahip olarak
		  Kütahya’nın tüm ilçelerinde hizmet verebilmekteyiz.

          Hizmetlerimizde her zaman başarılı olmuş kendisine sürekli yenilik ve başarı kazandıran ilkeler edinmiştir.

          Çalışmalarımız gerek bireysel olarak gerekse grup olarak hizmet verileceği yere göre değişmekte olup temizlikte
		  kullandığımız malzemelerde çalışılacak yere göre tespit edilmektedir.Bizler sadece görünen temizliğe değil aynı
		  zamanda temizlik yapılan yerin hijyenliğine de önem veririz.</p>
        </section>
